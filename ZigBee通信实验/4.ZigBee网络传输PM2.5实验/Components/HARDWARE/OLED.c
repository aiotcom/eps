//==========================================================
//	函数名称：	OLED_P6x8Str()
//
//	函数功能：	显示6*8一组标准ASCLL字符串
//
//	入口参数：	显示坐标(X,Y) y(0~7)
//
//	返回参数：	无
//
//	说明：		
//==========================================================
void OLED_P6x8Str(unsigned char x, unsigned char y,unsigned char ch[])
{
    unsigned char c=0,i=0,j=0;
    while (ch[j]!='\0')
    {
        c =ch[j]-32;
        if(x>126){x=0;y++;}
        OLED_Set_Pos(x,y);
        for(i=0;i<6;i++)
        OLED_WrDat(F6x8[c][i]);
        x+=6;
        j++;
    }
}


//==========================================================
//	函数名称：	OLED_P8x16Str()
//
//	函数功能：	显示8*16一组标准ASCLL字符串
//
//	入口参数：	显示坐标(X,Y) y(0~7)
//
//	返回参数：	无
//
//	说明：		
//==========================================================
void OLED_P8x16Str(unsigned char x, unsigned char y,unsigned char ch[])
{
    unsigned char c=0,i=0,j=0;
    while (ch[j]!='\0')
    {
        c =ch[j]-32;
        if(x>120){x=0;y++;}
        OLED_Set_Pos(x,y);
        for(i=0;i<8;i++)
        OLED_WrDat(F8X16[c*16+i]);
        OLED_Set_Pos(x,y+1);
        for(i=0;i<8;i++)
        OLED_WrDat(F8X16[c*16+i+8]);
        x+=8;
        j++;
    }
}


//==========================================================
//	函数名称：	OLED_P16x16Ch()
//
//	函数功能：	显示16*16点阵
//
//	入口参数：	显示坐标(X,Y) y(0~7)
//
//	返回参数：	无
//
//	说明：		
//==========================================================
void OLED_P16x16Ch(unsigned char x, unsigned char y, unsigned char N)
{
    unsigned char wm=0;
    unsigned int adder=32*N;
    OLED_Set_Pos(x , y);
    for(wm = 0;wm < 16;wm++)
    {
        OLED_WrDat(F16x16[adder]);
        adder += 1;
    }
    OLED_Set_Pos(x,y + 1);
    for(wm = 0;wm < 16;wm++)
    {
        OLED_WrDat(F16x16[adder]);
        adder += 1;
    } 	  	
}

//==========================================================
//	函数名称：	Draw_BMP()
//
//	函数功能：	显示BMP图片128X64
//
//	入口参数：	X:0~127  Y:0~7
//
//	返回参数：	无
//
//	说明：		
//==========================================================
void Draw_BMP(unsigned char x0, unsigned char y0,unsigned char x1, unsigned char y1,unsigned char BMP[])
{
  unsigned int j=0;
  unsigned char x,y;

  if(y1%8==0) y=y1/8;      
  else y=y1/8+1;
    for(y=y0;y<y1;y++)
    {
        OLED_Set_Pos(x0,y);
        for(x=x0;x<x1;x++)
        {      
            OLED_WrDat(BMP[j++]);
        }
    }
}


//==========================================================
//	函数名称：	OLED_DrawLineX
//
//	函数功能：	画水平线
//
//	入口参数：	x1,y1:起点坐标
//              x2:终点坐标 
//							Data:数据
//              
//	返回参数：	无
//
//	说明：		
//==========================================================
void OLED_DrawLineX(uint16 x1, uint16 y1, uint16 x2,uint16 Data)
{
    static uint32 i = 0;
    if(x1>126 || y1>7 || x2>126)
    {
       x1=0;y1=0;x2=0;
    }
    OLED_Set_Pos(x1,y1);
    
    for(i=0;i<(x2-x1);i++)
    {
       OLED_WrDat(Data);
    }
}


//==========================================================
//	函数名称：	OLED_DrawLineY
//
//	函数功能：	画竖直线
//
//	入口参数：	x1,y1:起点坐标
//                      y2:终点坐标 
//			Data:数据
//              
//	返回参数：	无
//
//	说明：		
//==========================================================
void OLED_DrawLineY(uint16 x1, uint16 y1, uint16 y2,uint16 Data)
{
    static uint32 i = 0;
    if(x1>126  || y1>7 || y2>7 )
    {
         x1=0;y1=0;y2=0;
    }
    
    for(i=y1;i<=y2;i++)
    {
        OLED_Set_Pos(x1,i);
        OLED_WrDat(Data);
    }	
}


