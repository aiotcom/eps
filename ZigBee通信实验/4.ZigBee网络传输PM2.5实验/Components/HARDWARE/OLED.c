//==========================================================
//	函数名称：	OLED_WrDat()
//
//	函数功能：	OLED写数据
//
//	入口参数：	无
//
//	返回参数：	无
//
//	说明：		
//==========================================================
void OLED_WrDat(unsigned char IIC_Data)
{
    IIC_Start();
    Write_IIC_Byte(0x78);
    Write_IIC_Byte(0x40);			//write data
    Write_IIC_Byte(IIC_Data);
    IIC_Stop();
}
//==========================================================
//	函数名称：	OLED_WrCmd()
//
//	函数功能：	OLED写命令
//
//	入口参数：	无
//
//	返回参数：	无
//
//	说明：		
//==========================================================
void OLED_WrCmd(unsigned char IIC_Command)
{
    IIC_Start();
    Write_IIC_Byte(0x78);       //Slave address,SA0=0
    Write_IIC_Byte(0x00);				//write command
    Write_IIC_Byte(IIC_Command);
    IIC_Stop();
}

//==========================================================
//	函数名称：	OLED_Set_Pos()
//
//	函数功能：	OLED设置坐标
//
//	入口参数：	无
//
//	返回参数：	无
//
//	说明：		
//==========================================================
void OLED_Set_Pos(unsigned char x, unsigned char y) 
{ 
    OLED_WrCmd(0xb0+y);
    OLED_WrCmd(((x&0xf0)>>4)|0x10);
    OLED_WrCmd((x&0x0f)|0x01);
} 

//==========================================================
//	函数名称：	OLED_Fill()
//
//	函数功能：	OLED全屏
//
//	入口参数：	无
//
//	返回参数：	无
//
//	说明：
//==========================================================
void OLED_Fill(unsigned char bmp_dat) 
{
  unsigned char y,x;
  for(y=0;y<8;y++)
  {
      OLED_WrCmd(0xb0+y);
      OLED_WrCmd(0x01);
      OLED_WrCmd(0x10);
      for(x=0;x<X_WIDTH;x++)
      {
          OLED_WrDat(bmp_dat);
      }
  }
}


//==========================================================
//	函数名称：	OLED_CLS()
//
//	函数功能：	OLED复位
//
//	入口参数：	无
//
//	返回参数：	无
//
//	说明：
//==========================================================
void OLED_CLS(void)
{
  unsigned char y,x;
  for(y=0;y<8;y++)
  {
      OLED_WrCmd(0xb0+y);
      OLED_WrCmd(0x01);
      OLED_WrCmd(0x10);
      for(x=0;x<X_WIDTH;x++)
      OLED_WrDat(0);
  }
}
