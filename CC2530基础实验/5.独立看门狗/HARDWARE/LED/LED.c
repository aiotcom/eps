#include "delay.h"
#include "LED.h"

//==========================================================
//	函数名称：	LED_Init()
//
//	函数功能：	初始化LED
//
//	入口参数：	无
//
//	返回参数：	无
//
//	说明：		
//==========================================================
void LED_Init(void)
{
   P0DIR |= 0x0C;      //P0.2、P0.3定义为输出
   P1DIR |= 0xC0;      //P1.6、P1.7定义为输出
   LED_OFF(); 
}
